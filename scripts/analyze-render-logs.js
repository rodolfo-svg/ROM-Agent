async function readInput() {
  let input;

  // Verificar se tem arquivo como argumento
  if (process.argv[2] && fs.existsSync(process.argv[2])) {
    console.log(`üìÑ Lendo arquivo: ${process.argv[2]}`);
    console.log('');
    input = fs.createReadStream(process.argv[2]);
  }
  // Verificar se tem input via pipe
  else if (!process.stdin.isTTY) {
    console.log('üìÑ Lendo de stdin (pipe)...');
    console.log('');
    input = process.stdin;
  }
  // Modo interativo
  else {
    console.log('üìÑ Cole os logs do Render abaixo e pressione Ctrl+D quando terminar:');
    console.log('   (ou use: node scripts/analyze-render-logs.js logs.txt)');
    console.log('');
    input = process.stdin;
  }

  const rl = readline.createInterface({
    input,
    crlfDelay: Infinity
  });

  let lineNumber = 0;
  for await (const line of rl) {
    lineNumber++;
    await analyzeLine(line, lineNumber);
  }
}

