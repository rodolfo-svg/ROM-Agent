function makeRequest(url, options = {}) {
  return new Promise((resolve, reject) => {
    const parsedUrl = new URL(url);
    const client = parsedUrl.protocol === 'https:' ? https : http;

    const reqOptions = {
      hostname: parsedUrl.hostname,
      port: parsedUrl.port,
      path: parsedUrl.pathname + parsedUrl.search,
      method: options.method || 'GET',
      headers: options.headers || {},
      timeout: TIMEOUT
    };

    const req = client.request(reqOptions, (res) => {
      let data = '';
      res.on('data', (chunk) => data += chunk);
      res.on('end', () => {
        try {
          const jsonData = data ? JSON.parse(data) : null;
          resolve({ status: res.statusCode, data: jsonData, headers: res.headers });
        } catch (e) {
          resolve({ status: res.statusCode, data: data, headers: res.headers });
        }
      });
    });

    req.on('error', (error) => reject(error));
    req.on('timeout', () => {
      req.destroy();
      reject(new Error('Request timeout'));
    });

    if (options.body) {
      req.write(typeof options.body === 'string' ? options.body : JSON.stringify(options.body));
    }

    req.end();
  });
}

async function testEndpoint(category, name, url, options = {}, expectedStatus = 200, validator = null) {
  log(`\nüîç Testando: [${category}] ${name}`, 'blue');
  stats.total++;

  try {
    const startTime = Date.now();
    const result = await makeRequest(url, options);
    const duration = Date.now() - startTime;

    const passed = result.status === expectedStatus;
    let validationPassed = true;
    let validationMsg = '';

    // Valida√ß√£o customizada
    if (validator && passed) {
      const validation = validator(result.data);
      validationPassed = validation.passed;
      validationMsg = validation.message || '';
    }

    const finalPassed = passed && validationPassed;

    if (finalPassed) {
      stats.passed++;
      log(`‚úÖ PASSOU - Status ${result.status} (${duration}ms)`, 'green');
      if (validationMsg) log(`   ${validationMsg}`, 'cyan');
    } else {
      stats.failed++;
      log(`‚ùå FALHOU - Status ${result.status}, esperado ${expectedStatus} (${duration}ms)`, 'red');
      if (!validationPassed) log(`   Valida√ß√£o: ${validationMsg}`, 'yellow');
      if (result.data && result.data.error) log(`   Erro: ${result.data.error}`, 'yellow');
    }

    stats.results.push({
      category,
      name,
      url,
      status: result.status,
      expected: expectedStatus,
      passed: finalPassed,
      duration,
      validation: validationMsg
    });

    return { passed: finalPassed, result, duration };

  } catch (error) {
    stats.failed++;
    log(`‚ùå ERRO - ${error.message}`, 'red');
    stats.results.push({
      category,
      name,
      url,
      error: error.message,
      passed: false
    });
    return { passed: false, error: error.message };
  }
}

